package ControlStatements;
/*
Syntax:
	
		do
		{
		   statement(s);
		
		}while( condition );	

 */
public class DoWhile {

	public static void main(String[] args) {
		int n = 1, y = 0;
        do {
            System.out.println("z:" + n);
            n++;
        } while (n <= y);
	}

}

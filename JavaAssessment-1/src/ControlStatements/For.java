package ControlStatements;
/*
Syntax:
	
	for ( init; condition; increment )
	{
	   statement(s);
	}		

 */
public class For {

	public static void main(String[] args) {
		 int n = 1, y= 5;

	        for (n = 1; n <= y; n = n + 1) {
	            System.out.println("output:" + n);
	        }
		
	}

}
